// src/models/Dish.js

const mongoose = require('mongoose');

// Define the Dish schema
const DishSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  image: {
    type: String,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

// Export the Dish model
module.exports = mongoose.model('Dish', DishSchema);
